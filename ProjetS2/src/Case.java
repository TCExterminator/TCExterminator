public class Case
{
	
  private char typeCase;
  
  /**
   * methode getTypeCase
   * 
   * cette methode permet de retourner le char typeCase
   * 
   * @return du char typeCase
   */
  public char getTypeCase()
  {
    return this.typeCase;
  }
  
  /**
   * methode setTypeCase
   * 
   * cette methode permet de modifier le typeCase de la classe
   * 
   * @param tyC le char du nouveau typeCase
   */
  public void setTypeCase(char tyC)
  {
    this.typeCase = tyC;
  }
  
}
